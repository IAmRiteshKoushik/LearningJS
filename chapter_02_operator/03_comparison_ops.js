// Comparison operators
let comp1 = 6;
let comp2 = 7;

console.log("comp1 == comp2", comp1 == comp2) 
// false
console.log("comp1 != comp2", comp1 != comp2)
// true
console.log("comp1 === comp2", comp1 === comp2)
// false as the data type and the content both do not match
console.log("comp1 !== comp2", comp1 !== comp2)
// true as the previous case is false (NOT == )
