// Switch statements in JavaScript
let day = 7;

switch(day){
    case 1:
        text = "Today is Monday";
        break;
    case 2:
        text = "Today is Tuesday";
        break;
    case 3:
        text = "Today is Wednesday";
        break;
    case 4:
        text = "Today is Thursday";
        break;
    case 5:
        text = "Today is Friday";
        break;
    case 6:
        text = "Today is Saturday";
        break;
    case 7:
        text = "Today is Sunday";
        break;
}

console.log(text)

